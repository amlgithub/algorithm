package day_0330;

/**
 * 链表结构体
 * @author aml
 * @date 2021/4/7 15:52
 */
public class ListNode {


    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }


}
